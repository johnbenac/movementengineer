import fs from 'node:fs/promises';
import path from 'node:path';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);
const loader = require('../src/core/markdownDatasetLoader.js');

const repoPath = process.argv[2] || process.env.ME_DEFAULT_DATA_REPO || '../catholic';
const outputPath =
  process.argv[3] || process.env.ME_DEFAULT_DATA_OUTPUT || 'src/runtime/bundledDefaultSnapshot.js';
const resolvedRepoPath = path.resolve(repoPath);
const resolvedOutputPath = path.resolve(outputPath);

const compiled = await loader.loadMovementDataset({
  source: 'local',
  repoPath: resolvedRepoPath
});

const snapshot = {
  ...compiled.data,
  __repoInfo: {
    owner: 'johnbenac',
    repo: 'catholic',
    ref: 'main',
    url: 'https://github.com/johnbenac/catholic',
    bundled: true
  },
  __repoSource: {
    source: 'bundled',
    repository: 'johnbenac/catholic'
  },
  __repoFileIndex: compiled.fileIndex || {},
  __repoRawMarkdownByPath: compiled.rawMarkdownByPath || {},
  __repoBaselineByMovement: loader.buildBaselineByMovement(compiled.data, compiled.specVersion),
  version: compiled.specVersion,
  specVersion: compiled.specVersion
};

const content = `/* This file is generated by tools/build-bundled-default.mjs. */\n(function () {\n  'use strict';\n\n  const globalScope = typeof globalThis !== 'undefined' ? globalThis : window;\n  globalScope.MovementEngineerBundledDefaultSnapshot = ${JSON.stringify(snapshot, null, 2)};\n})();\n`;

await fs.mkdir(path.dirname(resolvedOutputPath), { recursive: true });
await fs.writeFile(resolvedOutputPath, content, 'utf8');

console.log(`Wrote ${path.relative(process.cwd(), resolvedOutputPath)}`);
